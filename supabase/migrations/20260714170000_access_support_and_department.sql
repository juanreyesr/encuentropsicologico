create or replace function public.encuentro_psicologico_mask_email(p_email text)
returns text
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_local text;
  v_domain text;
  v_domain_name text;
  v_domain_tld text;
begin
  if p_email is null or position('@' in p_email) = 0 then return null; end if;
  v_local := split_part(p_email, '@', 1);
  v_domain := split_part(p_email, '@', 2);
  v_domain_name := split_part(v_domain, '.', 1);
  v_domain_tld := substring(v_domain from position('.' in v_domain));
  return left(v_local, 2) || repeat('*', greatest(2, length(v_local) - 3)) || right(v_local, 1) || '@' || left(v_domain_name, 1) || repeat('*', greatest(2, length(v_domain_name) - 2)) || right(v_domain_name, 1) || v_domain_tld;
end;
$$;

revoke all on function public.encuentro_psicologico_mask_email(text) from public, anon, authenticated;
grant execute on function public.encuentro_psicologico_mask_email(text) to service_role;

create or replace function public.encuentro_psicologico_lookup_by_phone(p_phone text)
returns jsonb
language plpgsql
security definer
set search_path = public, auth, pg_temp
as $$
declare
  v_phone text := regexp_replace(coalesce(p_phone, ''), '[^0-9]', '', 'g');
  v_user_id uuid;
  v_email text;
  v_modality text;
  v_status text;
begin
  select p.user_id, u.email into v_user_id, v_email
  from public.encuentro_psicologico_profiles p
  join auth.users u on u.id = p.user_id
  where regexp_replace(p.phone, '[^0-9]', '', 'g') = v_phone
  order by p.created_at desc
  limit 1;

  if v_user_id is null then
    select r.user_id, r.email into v_user_id, v_email
    from public.encuentro_psicologico_registrations r
    where regexp_replace(r.phone, '[^0-9]', '', 'g') = v_phone
      and r.status in ('confirmed', 'waitlist')
    order by r.created_at desc
    limit 1;
  end if;

  if v_email is null then
    return jsonb_build_object('found', false);
  end if;

  select modality, status into v_modality, v_status
  from public.encuentro_psicologico_registrations
  where regexp_replace(phone, '[^0-9]', '', 'g') = v_phone
    and status in ('confirmed', 'waitlist')
  order by created_at desc
  limit 1;

  return jsonb_build_object('found', true, 'maskedEmail', public.encuentro_psicologico_mask_email(v_email), 'modality', v_modality, 'status', v_status);
end;
$$;

revoke all on function public.encuentro_psicologico_lookup_by_phone(text) from public, anon, authenticated;
grant execute on function public.encuentro_psicologico_lookup_by_phone(text) to service_role;

create or replace function public.encuentro_psicologico_auth_user_id_by_phone_email(p_phone text, p_email text)
returns uuid
language sql
security definer
set search_path = public, auth, pg_temp
as $$
  select p.user_id
  from public.encuentro_psicologico_profiles p
  join auth.users u on u.id = p.user_id
  where regexp_replace(p.phone, '[^0-9]', '', 'g') = regexp_replace(coalesce(p_phone, ''), '[^0-9]', '', 'g')
