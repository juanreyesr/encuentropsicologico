create or replace function public.encuentro_psicologico_community_resource_reward()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
begin
  if new.status = 'approved' and old.status is distinct from 'approved' then
    insert into public.encuentro_psicologico_community_reward_events
      (owner_user_id, resource_id, event_type, actor_user_id)
    values (new.owner_user_id, new.id, 'contribution', new.owner_user_id)
    on conflict (resource_id, event_type, actor_user_id) do nothing;
  elsif old.status = 'approved' and new.status is distinct from 'approved' then
    delete from public.encuentro_psicologico_community_reward_events where resource_id = new.id;
  end if;
  return new;
end;
$$;

drop trigger if exists encuentro_psicologico_community_resource_reward_trigger
  on public.encuentro_psicologico_community_resources;
create trigger encuentro_psicologico_community_resource_reward_trigger
after update of status on public.encuentro_psicologico_community_resources
for each row execute function public.encuentro_psicologico_community_resource_reward();

create or replace function public.encuentro_psicologico_community_download_reward()
returns trigger
language plpgsql
set search_path = public, pg_temp
as $$
declare
  v_owner uuid;
  v_status text;
begin
  select owner_user_id, status into v_owner, v_status
  from public.encuentro_psicologico_community_resources
  where id = new.resource_id;

  if v_status = 'approved' and v_owner is distinct from new.downloader_user_id then
    insert into public.encuentro_psicologico_community_reward_events
      (owner_user_id, resource_id, event_type, actor_user_id)
    values (v_owner, new.resource_id, 'download', new.downloader_user_id)
    on conflict (resource_id, event_type, actor_user_id) do nothing;
  end if;
  return new;
end;
$$;

