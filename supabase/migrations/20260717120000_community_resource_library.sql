create table if not exists public.encuentro_psicologico_community_categories (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  is_default boolean not null default false,
  is_active boolean not null default true,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  constraint encuentro_psicologico_community_categories_name_check check (char_length(trim(name)) between 2 and 60)
);

create table if not exists public.encuentro_psicologico_community_resources (
  id bigint generated by default as identity primary key,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  category_id bigint not null references public.encuentro_psicologico_community_categories(id),
  title text not null,
  description text,
  source_author text,
  rights_basis text not null,
  responsibility_accepted_at timestamptz not null,
  storage_path text not null unique,
  original_filename text not null,
  mime_type text not null,
  size_bytes bigint not null,
  status text not null default 'pending',
  moderation_reason text,
  created_at timestamptz not null default now(),
  approved_at timestamptz,
  removed_at timestamptz,
  constraint encuentro_psicologico_community_resources_title_check check (char_length(trim(title)) between 3 and 160),
  constraint encuentro_psicologico_community_resources_rights_check check (rights_basis in ('own_work', 'permission', 'public_domain', 'open_license', 'legal_source')),
  constraint encuentro_psicologico_community_resources_status_check check (status in ('pending', 'approved', 'rejected', 'removed')),
  constraint encuentro_psicologico_community_resources_size_check check (size_bytes > 0 and size_bytes <= 26214400)
);

create table if not exists public.encuentro_psicologico_community_downloads (
  id bigint generated by default as identity primary key,
  resource_id bigint not null references public.encuentro_psicologico_community_resources(id) on delete cascade,
  downloader_user_id uuid not null references auth.users(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (resource_id, downloader_user_id)
);

create table if not exists public.encuentro_psicologico_community_reward_events (
  id bigint generated by default as identity primary key,
  owner_user_id uuid not null references auth.users(id) on delete cascade,
  resource_id bigint not null references public.encuentro_psicologico_community_resources(id) on delete cascade,
  event_type text not null,
  actor_user_id uuid not null references auth.users(id) on delete cascade,
  points integer not null default 1,
  created_at timestamptz not null default now(),
  constraint encuentro_psicologico_community_reward_type_check check (event_type in ('contribution', 'download')),
  constraint encuentro_psicologico_community_reward_points_check check (points = 1),
  unique (resource_id, event_type, actor_user_id)
);

