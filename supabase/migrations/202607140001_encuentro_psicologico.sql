create table if not exists public.encuentro_psicologico_registrations (
  id bigint generated by default as identity primary key,
  modality text not null check (modality in ('presencial', 'virtual')),
  name text not null,
  email text not null,
  phone text not null,
  attendee_type text not null default 'general',
  profession text,
  license text,
  institution text,
  country text not null default 'Guatemala',
  status text not null check (status in ('confirmed', 'waitlist', 'cancelled')),
  created_at timestamptz not null default now()
);

create index if not exists encuentro_psicologico_registrations_capacity_idx
  on public.encuentro_psicologico_registrations (modality, status);

alter table public.encuentro_psicologico_registrations enable row level security;
create policy "encuentro_psicologico_registrations_server_only"
  on public.encuentro_psicologico_registrations for all to anon, authenticated
  using (false) with check (false);
revoke all on table public.encuentro_psicologico_registrations from anon, authenticated;
grant all on table public.encuentro_psicologico_registrations to service_role;
grant usage, select on sequence public.encuentro_psicologico_registrations_id_seq to service_role;

create table if not exists public.encuentro_psicologico_content (
  id text primary key,
